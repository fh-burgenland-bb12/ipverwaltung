<?php

namespace Ipverwaltung\Model\Firma;
use Zend\InputFilter\InputFilter;

class FirmaInputFilter extends InputFilter
{

    public function __construct()
    {
        parent::__construct();
		$this->add(array(
            'name' => 'firmaId',
            'required' => true,
            'filters' => array(
                array('name' => 'StripTags'),
                array('name' => 'StringTrim'),
                array('name' => 'Digits')
                ),
            'validators' => array(
                array('name' => 'Digits'),
                array(
                    'name' => 'Between',
                    'options' => array(
                        'min' => 0,
                        'max' => 65535
                        )
                    )
                )
            ));
		$this->add(array(
            'name' => 'name',
            'required' => true,
            'filters' => array(
                array('name' => 'StripTags'),
                array('name' => 'StringTrim'),
                array('name' => 'Digits')
                ),
            'validators' => array(array(
                    'name' => 'StringLength',
                    'options' => array(
                        'min' => 0,
                        'max' => '150'
                        )
                    ))
            ));
	}
}
